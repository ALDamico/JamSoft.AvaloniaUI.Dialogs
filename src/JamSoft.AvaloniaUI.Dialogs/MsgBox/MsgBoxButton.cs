namespace JamSoft.AvaloniaUI.Dialogs.MsgBox;

/// <summary>
/// The message box button enum
/// </summary>
public enum MsgBoxButton
{
    /// <summary>
    /// Show the OK button
    /// </summary>
    Ok,
    
    /// <summary>
    /// Show the OK and Cancel buttons
    /// </summary>
    OkCancel,
    
    /// <summary>
    /// Show the Yes and No buttons
    /// </summary>
    YesNo,
    
    /// <summary>
    /// Show the Yes, No and Cancel buttons
    /// </summary>
    YesNoCancel
}